    public void mouseDragged(MouseEvent e) {
//        x = jPanel2.getMousePosition().x;
//        y = jPanel2.getMousePosition().y;

        g = jPanel2.getGraphics();
        if (option.equals("drow")) {
            g.setColor(col);
        } else if (option.equals("erase")) {
            g = jPanel2.getGraphics();g.setColor(Color.WHITE);
        }

        g.fillRect(e.getX(), e.getY(), stroke, stroke);
    }

